#include "drivers/wdt.h"


/*************************************************************
 * wdtDisable
 *
 * Comment:
 * Disable the watchdog timer
 ************************************************************/
void wdtDisable(void)
{
	volatile avr32_wdt_t * wdt = &AVR32_WDT;
	wdt->ctrl = 0x55 << WDT_KEY_OFFSET;
	wdt->ctrl = 0xaa << WDT_KEY_OFFSET;
}